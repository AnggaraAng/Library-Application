     public void pencarian_buku(){
        String cari = text_search.getText();
        
       Object[] Baris={"kode_buku","judul_buku","pengarang","penerbit","tahun_terbit","tempat_terbit","jumlah","tanggal"};
       tabmode = new DefaultTableModel(null, Baris);
       Table_buku.setModel(tabmode);

       con = connection.getConnection();
       
       try{
           String sql="Select * from tb_buku where "
                   + "kode_buku like '%"+cari+"%' "
                   + "OR judul_buku like '%"+cari+"%' "
                   + "OR pengarang like '%"+cari+"%' "
                   + "OR penerbit like '%"+cari+"%' "
                   + "OR tahun_terbit like '%"+cari+"%' "
                   + "OR tempat_terbit like '%"+cari+"%' "
                   + "OR jumlah like '%"+cari+"%' "
                   + "OR tanggal like '%"+cari+"%' "
                   + "order by kode_buku asc";
           java.sql.Statement stmt=con.createStatement();
           java.sql.ResultSet rslt=stmt.executeQuery(sql);
           while(rslt.next()){
               String kode_buku=rslt.getString("kode_buku");
               String judul_buku=rslt.getString("judul_buku");
               String pengarang=rslt.getString("pengarang");
               String penerbit=rslt.getString("penerbit");
               String tahun_terbit=rslt.getString("tahun_terbit");
               String tempat_terbit=rslt.getString("tempat_terbit");
               String jumlah=rslt.getString("jumlah");
               String tanggal=rslt.getString("tanggal");
               
               String[] dataField={kode_buku,judul_buku,pengarang,penerbit,tahun_terbit,tempat_terbit,jumlah,tanggal};
               tabmode.addRow(dataField);
           }
       }
       catch(Exception ex){
       }
    }
    
