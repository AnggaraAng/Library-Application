    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        String nim="", nama="", fakultas="", program_studi="", tahun_angkatan="", jenis_kelamin="", alamat="", no_telp="";
        
        nim = text_nim.getText();
        nama = text_nama.getText();
        fakultas = text_fakultas.getText();
        program_studi = text_prodi.getText();
        tahun_angkatan = text_ta.getText();
        jenis_kelamin = (String) combo_jenis_kelamin.getSelectedItem();
        alamat = text_alamat.getText();
        no_telp = text_no_telp.getText();
       
        con = connection.getConnection();
        try {
            
            String sql = "insert into tb_mahasiswa values (?,?,?,?,?,?,?,?)";
            PreparedStatement stat = (PreparedStatement) 
            con.prepareStatement(sql);
            
            stat.setString(1, nim);
            stat.setString(2, nama);
            stat.setString(3, fakultas);
            stat.setString(4, program_studi);
            stat.setString(5, tahun_angkatan);
            stat.setString(6, jenis_kelamin);
            stat.setString(7, alamat);
            stat.setString(8, no_telp);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Menyimpan data BERHASIL","Informasi", JOptionPane.INFORMATION_MESSAGE);
            reset();
            tampil_Table_mahasiswa();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menyimpan data GAGAL","Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

