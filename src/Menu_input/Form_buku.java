    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        String kode_buku="", judul_buku="", pengarang="", penerbit="", tahun_terbit="", tempat_terbit="", jumlah="", tanggal;

        kode_buku = text_kode_buku.getText();
        judul_buku = text_judul_buku.getText();
        pengarang = text_pengarang.getText();
        penerbit = text_penerbit.getText();
        tahun_terbit = text_tahun_terbit.getText();
        tempat_terbit = text_tempat_terbit.getText();
        jumlah = text_jumlah.getText();
        tanggal = text_tanggal.getText();

        con = connection.getConnection();
        try {

            String sql = "insert into tb_buku values (?,?,?,?,?,?,?,?)";
            PreparedStatement stat = (PreparedStatement)
            con.prepareStatement(sql);

            stat.setString(1, kode_buku);
            stat.setString(2, judul_buku);
            stat.setString(3, pengarang);
            stat.setString(4, penerbit);
            stat.setString(5, tahun_terbit);
            stat.setString(6, tempat_terbit);
            stat.setString(7, jumlah);
            stat.setString(8, tanggal);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Menyimpan data BERHASIL","Informasi", JOptionPane.INFORMATION_MESSAGE);
            reset();
            tampil_Table_buku();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menyimpan data GAGAL","Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

