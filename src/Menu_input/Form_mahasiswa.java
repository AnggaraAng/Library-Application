    private DefaultTableModel tabmode;
        public void tampil_Table_mahasiswa(){
        Object []baris = {"nim","Nama","fakultas","program_studi","tahun_angkatan","jenis_kelamin","alamat","no_telp"};
        tabmode = new DefaultTableModel(null, baris);
        Table_mahasiswa.setModel(tabmode);
        con = connection.getConnection();
        try {
            String sql = "select * from tb_mahasiswa order by nim asc";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String nim = hasil.getString("nim");
                String nama = hasil.getString("nama");
                String fakultas = hasil.getString("fakultas");
                String program_studi = hasil.getString("program_studi");
                String tahun_angkatan = hasil.getString("tahun_angkatan"); 
                String jenis_kelamin = hasil.getString("jenis_kelamin");
                String alamat = hasil.getString("alamat"); 
                String no_telp = hasil.getString("no_telp");
                String[] data = {nim, nama, fakultas, program_studi, tahun_angkatan, jenis_kelamin, alamat, no_telp};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL","Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
