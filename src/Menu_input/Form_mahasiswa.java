    public void pencarian_mahasiswa(){
        String cari = text_search.getText();
        
       Object[] Baris={"nim","nama","fakultas","program_studi","tahun_angkatan","jenis_kelamin","alamat","no_telp"};
       tabmode = new DefaultTableModel(null, Baris);
       Table_mahasiswa.setModel(tabmode);

       con = connection.getConnection();
       
       try{
           String sql="Select * from tb_mahasiswa where "
                   + "nim like '%"+cari+"%' "
                   + "OR nama like '%"+cari+"%' "
                   + "OR fakultas like '%"+cari+"%' "
                   + "OR program_studi like '%"+cari+"%' "
                   + "OR tahun_angkatan like '%"+cari+"%' "
                   + "OR jenis_kelamin like '%"+cari+"%' "
                   + "OR alamat like '%"+cari+"%' "
                   + "OR no_telp like '%"+cari+"%' "
                   + "order by nim asc";
           java.sql.Statement stmt=con.createStatement();
           java.sql.ResultSet rslt=stmt.executeQuery(sql);
           while(rslt.next()){
               String nim=rslt.getString("nim");
               String nama=rslt.getString("nama");
               String fakultas=rslt.getString("fakultas");
               String program_studi=rslt.getString("program_studi");
               String tahun_angkatan=rslt.getString("tahun_angkatan");
               String jenis_kelamin=rslt.getString("jenis_kelamin");
               String alamat=rslt.getString("alamat");
               String no_telp=rslt.getString("no_telp");
               
               String[] dataField={nim,nama,fakultas,program_studi,tahun_angkatan,jenis_kelamin,alamat,no_telp};
               tabmode.addRow(dataField);
           }
       }
       catch(Exception ex){
       }
    }
    
